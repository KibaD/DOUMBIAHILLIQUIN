            <h2> <center> <b> Recette Cake aux courgettes et parmesan </center> </b></h2>
            <p><b> Difficulé : </b></p>
                <div class="progress">
                    <div class="progress-bar progress-bar-danger" role="progressbar" aria-valuenow="20" aria-valuemin="0" aria-valuemax="100" style="width: 20%">
                    <span class="sr-only">80% Complete (danger)</span>
                    </div>
                </div>
            <p>  <b>Ingrédients / pour 6 personnes </b></br>
            - 250 g de courgettes</br>
            - 100 g de parmesan râpé (fraîchement si possible)</br>
            - 200 g de farine</br>
            - 3 oeufs</br>
            - 100 g d'emmental râpé</br>
            - 1 sachet de levure chimique</br>
            - 10 cl d'huile d'olive</br>
            - 10 cl de lait</br>
            - 20 g de beurre</br>
            - une poignée de basilic haché</br>
            - sel et poivre </p>
            <p> <b>Préparation : </b></br>
                1 Lavez les courgettes, puis râpez-les finement avant de les faire blondir dans une poêle avec le beurre. </br>
                2 Battez les oeufs avec l'huile et le lait, salez et poivrez. Mélangez la levure et la farine et ajoutez-y le mélange oeufs-huile-lait progressivement en mélangeant pour éviter les grumeaux.
                3 Ajoutez les fromages, le basilic et les courgettes. </br>
                4 Préchauffez le four à thermostat 6 (180°C). Beurrez et farinez un moule à cake et versez-y la préparation. </br>
                Pour finir : Faites cuire pendant environ 50 minutes. Le cake est cuit quand en le piquant avec un couteau la lame ressort sèche.
            </p>
