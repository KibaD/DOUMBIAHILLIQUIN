    <article>
        <h2> <center> <b> Recette Millefeuille salé aubergine, tomate, courgette, thon & mozzarella </center> </b> </h2>
        <p> <b>Difficulté : </b> </p>

        <div class="progress">
        <div class="progress-bar progress-bar-danger" role="progressbar" aria-valuenow="70" aria-valuemin="0" aria-valuemax="100" style="width: 70%">
            <span class="sr-only">80% Complete (danger)</span>
        </div>
        </div>

        <p>  <b>Ingrédients / pour 2 personnes : </b>
- 9 feuilles de brick </br>
- 40 grammes de beurre demi-sel</br>
- 1 cuillère à soupe de miel</br>
- 1 aubergine</br>
- 1 courgette</br>
- 185 grammes de thon en boîte</br>
- 12 tomates cerise</br>
- 250 grammes de mozzarella</br>
- 2 cuillères à soupe de sauce tomate (type trio de poivrons aubergines Sacla)</br>
- Huile d'olive</br>
- 2 cuillères à soupe de jus de citron vert</br>
- Herbes de Provence</br>
- Sel & poivre </p>

        <p> <b>Préparation : </b> </br>
1 Commencez par la découpe de vos feuilles de brick. Si vous souhaitez faire de ce plat une entrée, découpez 36 carrés de 10 centimètres sur 10 (soit 4 carrés sur une feuille de brick standard). En revanche, si vous souhaitez faire de ce plat votre plat principal, découpez 18 rectangles de 20 centimètres sur 10 (soit 2 rectangles sur une feuille de brick standard). Dans une petite casserole, faîtes fondre le beurre avec le miel à feu doux. Déposez vos feuilles de brick sur du papier cuisson et badigeonnez-les du mélange avec un pinceau. Enfournez-les ensuite 5 minutes à 180°C pour les faire dorer. Reconduisez l'expérience si une seule fournée ne peut suffire pour contenir toutes les feuilles de brick. </br>
2 Une fois les feuilles de brick sorties du four, réservez dans une assiette le temps de préparer la suite. </br>
3 Lavez ensuite l'aubergine et découpez-la en 8 tranches assez fines. Placez ces tranches sur du papier cuisson à nouveau et arrosez-les d'huile d'olive, d'herbes de Provence, de sel et de poivre. Enfournez-les pendant 15 minutes environ à 180°C. Une fois les tranches d'aubergine sorties du four, réservez dans du papier absorbant pour éliminer l'excédent de graisse. </br>
4 Pendant la cuisson des aubergines, égouttez et émiettez le thon dans un saladier. Incorporez la courgette et les tomates cerises lavées et coupées en brunoise (en petits dés). Assaisonnez avec du sel, du poivre et le jus de citron vert et mélangez. </br>
Pour finir : Une fois ces étapes terminées, il ne vous reste plus qu'à procéder au montage des millefeuille. Premier niveau: Superposez 3 feuilles de brick. Étalez une cuillère à soupe de sauce tomate. Placez 2 tranches d'aubergine, puis une couche du mélange courgette / thon / tomate recouverte de 3 rondelles de mozzarella. Recouvrez de 3 nouvelles feuilles de brick. Deuxième niveau: Déposez à nouveau 2 tranches d'aubergine, le mélange courgette / thon / tomate réparti équitablement, 3 rondelles de mozzarella et déposez 3 feuilles de brick pour terminer. Votre millefeuille est terminé. Il ne vous reste plus qu'à décider si vous souhaitez le déguster chaud ou froid ! Si vous souhaitez le manger chaud, 20 minutes dans un four à 180°c devraient suffire.
        </p>
