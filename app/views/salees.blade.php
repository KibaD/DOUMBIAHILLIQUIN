        <h2> <center> <b>Tarte salée chèvre-saumon fumé et poireaux</center> </b></h2>
        <p> <b> Difficulté : </b> </p>
        <div class="progress">
        <div class="progress-bar progress-bar-danger" role="progressbar" aria-valuenow="35" aria-valuemin="0" aria-valuemax="100" style="width: 35%">
            <span class="sr-only">80% Complete (danger)</span>
        </div>
        </div>

        <p>  <b>Ingrédients / pour 6 personnes </b></p>
- 1 pâte feuilleté </br>
- lardons de saumon fumé </br>
- 2 blancs de poireaux </br>
- 1 brique crème de soja </br>
- 3 œufs </br>
- poudre du curcuma </p>
<p> <b>Préparation : </b> </br>
1 Etaler la pate feuilleté dans un plat </br>
2 Etaler des lardons de saumon fumé </br>
3 Couper en effiloché les poireaux, les mettre avec les lardons de saumon et couper des morceaux de chèvre et les déposer également </br>
4 mélanger 3 oeufs + 1 brique crême de soja + poudre du curcuma</br>
5 verser la préparation sur la pate et lardons/poireaux/chèvre</br>
Pour finir : faire cuire 40 mn au four à 200°C
        </p>
