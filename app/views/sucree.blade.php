<article>
<h2> <center> <b>La reine de Saba </center> </b></h2>
            <p><b> Difficulé : </b></p>
<div class="progress">
<div class="progress-bar progress-bar-danger" role="progressbar" aria-valuenow="70" aria-valuemin="0" aria-valuemax="100" style="width: 70%">
<span class="sr-only">80% Complete (danger)</span>
</div>
</div>
<p> <b> Ingrédients / pour 6 personnes</b>  </br>
- 125 g de chocolat noir pâtissier</br>
- 100 g de beurre</br>
- 100 g de sucre</br>
- 3 oeufs</br>
- 75 g de poudre d'amande (facultatif)</br>
- 50 g de farine tamisée</br>
- 1 sachet de sucre vanillé</p>

<p> <b>Préparation : </b></br>
1 Faites fondre 125 g de chocolat au bain-marie avec 2 cuillerées à soupe d'eau. </br>
2 Dans une terrine, travaillez le beurre ramolli en pommade. Versez le chocolat fondu et légèrement refroidi. Mélangez bien. Ajoutez les jaunes d'oeufs un par un, puis le sucre en poudre et la poudre d'amandes puis le sucre vanillé. </br>
3 Incorporez ensuite peu à peu la farine. Travaillez le mélange jusqu'à obtenir une pâté bien lisse et homogène. </br>
4 Préchauffez le four thermostat 6 (180°C). </br>
5 Battez les blancs d'oeufs en neige très fermes avec une pincée de sel. Incorporez-les petit à petit et très délicatement au mélange. </br>
6 Versez la préparation dans un moule à manqué carré, beurré et légèrement fariné. Enfournez et laissez cuire pendant 35 mn environ. </br>
7 Dès que le gâteau est cuit démoulez-le sur une grille et laissez-le refroidir complètement.
8 30 mn avant de servir le gâteau, préparez le glaçage : faites fondre le chocolat au bain-maire avec l'eau et un peu de sucre glacé. Dès qu'il est bien lisse, versez-le sur le gâteau et bien l'étaler.
Pour finir : Servez le gâteau entier ou coupé en parts. Pour plus de raffinement accompagnez-le de glace vanille et de rondelles de mandarines confites avec des feuilles de menthe fraîche.
</p>
